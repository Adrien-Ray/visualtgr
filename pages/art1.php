Développeur web mobile

Il effectue la réalisation technique et le développement informatique d’applications pour mobiles, smartphones et autres tablettes.

Ses responsabilités sont de tenir compte des spécificités de l’affichage des applications sur les différents outils.

Pour cela, il utilise différents langages selon les systèmes d’exploitation :

    C / Objective C / HTML5 (iOS)
    C / C# / PHP / Java (Android)

Deux possibilités s’offrent à lui :

    Adapter un site internet classique pour une application mobile
    Créer une application mobile qui n’a pas de site internet existant.

Qualités recherchées

    polyvalence
    autonomie
    respect des délais
    veille technologique

Où exerce t-on ces métiers ?

Cette activité s’exerce au sein de sociétés de services, d’entreprises, de collectivités territoriales, d’entreprises publiques, en indépendant etc.

Elle varie selon la taille de la structure (polyvalence, spécialisation en gestion du projet, programmation, recettes, etc.) et la responsabilité (développement, projet, etc).

Le salaire est également fonction de ces différents paramètres. En moyenne, un développeur web junior peut percevoir une rémunération brute mensuelle de 2100 €. Mais certains atteignent les étoiles tels les développeurs de la Silicon Valley qui peuvent recevoir un salaire de 500 000 $ … cela laisse rêveur !
